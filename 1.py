operator_1=float(input("Enter the first operator"))
operator_2=float(input("Enter the second operator"))
operend_=input("Choose an operend(+,-,*,/), ")
if operend_ == "+" :
    print(operator_1 + operator_2)
elif operend_ == "-" :
    print(operator_1 - operator_2)
elif operend_ == "*" :
    print(operator_2 * operator_1)
elif operend_ == "/" :
    print(operator_1 / operator_2)
print('Thank you')